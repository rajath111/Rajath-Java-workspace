package packageEcample;

public class Math1 {
	public static void main(String...strings) {
		System.out.println(Math.ceil(12.3));
		System.out.println(Math.floor(12.3));
		System.out.println(Math.round(12.3872));
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.abs(-12.3));
		System.out.println(Math.exp(12));
		System.out.println(Math.floorDiv(4, 3));
		System.out.println(Math.rint(12.6));
		System.out.println(Math.random());
		System.out.println((int)Math.max(12,9.5));
	}
}
